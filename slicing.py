# Working with slices

list1 = [12, 3, 4, 6, 7, 71, 2, 3, 4, 6, 7, 8, 9, 0]

# The Sice method takes three params start,stop and a step

# Reversing and printing out steps of 2
print(list1[::-2])

# Working with Strings

request = 'https://Woolan.com'

# Printing out the https only
print(request[:5])

# Printing out the domain
print(request[8:])

# Reversing the String name

print(request[::-1])
